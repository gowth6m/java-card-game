package main;

public class Card {

    private final int value;

    /**
     * Constructor for main.Card.
     * @param v Face value of the main.Card
     */
    public Card(int v) {
        value = v;
    }

    /**
     * Getter for face value of main.Card.
     * @return Face value of the main.Card
     */
    public int getValue(){
        return value;
    }

}
